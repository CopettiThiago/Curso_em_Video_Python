numero = int(input('Informe um número : '))

dobro = numero * 2
triplo = numero * 3
raiz = numero ** (1/2)

print(f'O dobro do numero {numero} é : {dobro}. \nSeu triplo é :{triplo}.  \nSua raiz quadrada é {raiz:.2f}')

